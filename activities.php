    <h3>Fuel Reduction</h3>

    <p>Fuel Reduction burns are often done on the ridge. The group assists by providing feedback on the ecological impacts of the proposed burn.</p>

    <h3>Herbarium</h3>

    <p>The group has a signficant herbarium. Specimens are shared with the scientific  community (e.g. National Botanical Gardens).</p>

    <h3>Photography</h3>

    <p>The group maintains a collection of photographs that relate to the ridge. These photos are old and new and cover all aspects of the ridge from animals and birds to the people and work. The photos are used to help monitor the changes in the ridge as well as  providing content for the web site.</p>

