    <h2>Contribute</h2>

    <p>The Cooleman Ridge Parkcare Group needs your help!</p>

    <p>We believe everybody can help the work the group does, no matter what their age, health, interest, skill-level or experience. Your contribution can be regular or adhoc, big or small - every little bit helps!</p>

    <p>To help out, you don't even need to make a special event of your work. Many people help out while just 'doing something else' on the ridge (e.g. walking the dog).</p>

    <p>Many members make various contributions. By contributing in just one area you can free another member to do something that you can't or don't want to do (e.g weeding).</p>

    <p>Here is a list of the many ways people (members and non members) contribute and how you too can contribute.</p>

    <h3>Weeding</h3>

    <p>Yes, weeding! Weeding can be surprisingly satisfying and even addictive. Once you start, it can be hard to stop.</p>

    <p>Weeding does not necessarily mean you have to attend <a href="meetings.php#group">group meetings</a> or have an <a href="groupareas.php">individual area</a>. Many people weed just when they go for a walk on the ridge every day and pull out a hundred weeds each time.</p>

    <p>Many people have a favourite weed (e.g <a href="weeds.php#fleabane">fleabane</a>, <a href="weeds.php#verbascum">verbascum</a>, <a href="weeds.php#stjohnswort">wort</a>, etc). They whack the weed wherever they see it.</p>

    <p>The combined effect of lots of people doing a little bit of weeding can have a big impact overtime.</p>

    <p>You can help by adopting a favourite weed and pulling it out whenever you can.</p>

    <h3>Surveying</h3>

    <p>Surveying is a great way to contribute if you do not want to get your &quot;hands dirty&quot;. You can help out with one any of the various surveys that the group conducts, such as surveys about <a href="plants.php">plants</a>, <a href="animals.php">animals</a>, <a href="woodyweeds.php">woody weeds</a>, <a href="fires.php">fires</a>, <a href="trees.php">trees</a> or <a href="blackberrys.php">blackberrys</a>.</p>

    <p>Surveying can be fun and you can learn a lot about the ridge doing it. Surveying does not have to involve a lot of time or effect and can normally be done while doing something else on the ridge.</p>

    <p>Surveying can be a great way to stimulate interest in the ridge and help other people to contribute to the work of the group.</p>

    <h3>Newsletter</h3>

    <p>The monthly newsletter is an important way of communicating information about our group, our ridge and our work.</p>

    <p>The newsletter is always in need of good content. If you can contribute information, an article, a photo, etc. then please do.</p>

    <p>Desktop publishing skills are also appreciated.</p>

    <h3>Photography</h3>

    <p>Photos are a great way of recording the progress of the work performs on the ridge. Photos are also a great way of promoting the ridge and the work we do on it.

    <p>The group needs photographers of any standard just to take regular photos of things like work areas, trees, fires, dams, etc.</p>

    <p>The photos can then be added to one of our surveys, the newsletter or content elsewhere on this web site.</p>

    <h3>Administration</h3>

    Administration is a significant part of what some in the group do. This can include attending briefings, information sessions and various other meetings. These can be both informative and interesting but valuable ways of promoting the the work the group does.

    <h3>Guided Walks</h3>

    <p>Guided Walks are valuable activity to promote the work the group does. If you have specialised knowledge about a particular aspect of the ridge, you can help the group by leading a guided walk to share your knowledge with the rest of the group and the broader community.</p>

    <h3>Rubbish Collection</h3>

    <p>There are unfortunately litters on the ridge. If you see some litter, please just pick it up.</p>

    <h3>Web Site Content</h3>

    <p>If you have some information that you think may be valuable for this web site, then please let us know, no matter how small. If you have a idea for a new page or section then let us know.</p>

    <h3>Website Development</h3>

    <p>You can help with improving and maintaining this website. If you have some web development skills (graphic design, programming, database administration, web hosting, etc), then we can find something for you to do!</p>







