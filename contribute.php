    <h2>Contribute</h2>

    <p>The Cooleman Ridge Parkcare group needs your help!</p>

    <p>You can help the group by doing any of the below. You don't have to do it regularly. You can just do it one off.</p>

    <p>There are many ways you can contribute. There is something for everybody to do no matter what your age, health, interest, experience</p>

    <p>Many people help out while doing other things on the ridge (eg. walking the dog).</p>

    <h3>Weeding</h3>

    <p>You don't need attend group meetings or have an individual area to help with the weeding. If you go for a walk on the ridge every day and pull out a hundred weeds each time, this can have a big impact overtime.</p>

    <h3>Rubbish Collection</h3>

    <p>There are unfortunately bogans on the ridge. They leave the rubbish. If you see some, please just pick it up. Many members pick up rubbish and if you do it, then it is one less thing that they have to do and can spend the time doing something else</p>

    <h3>Newsletter</h3>

    <p>The monthly newsletter is an important way of communicating information about our group, our ridge and our work</p>

    <p>The newsletter is always in need of good content. If you can contribute an article or photo, then please do</p>

    <p>Desktop publishing skills are also appreciated</p>

    <h3>Web Site Content</h3>

    <p>If you have some information that you think may be valualbe please let us know, no matter how small. If you have a idea for a new page or section then let us know.</p>

    <h3>Website</h3>

    <p>You can help with improving and maintaining this website. If you have some web development skills (graphic design, programming, database administration, web hosting, etc), then we can find something for you to do.</p>

    <h3>Photography</h3>

    <p>Donate your photos to the group.</p>

    <h3>Surveying</h3>

    <p>Helping on one of the groups many surveys is a great way to contribute if you do not want to get your &quot;hands dirty&quot;</p>



