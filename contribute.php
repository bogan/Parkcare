    <h2>Contribute</h2>

    <p>The Cooleman Ridge Parkcare Group needs your help!</p>

    <p>There is something for everybody to do no matter what your age, health, interest, experience. You don't have to do it regularly. You can just do it one off.</p>

    <p>Here is a list of the many ways you can contribute.</p>

    <p>You don't even need to make a special event of your work. Many people help out while just 'doing something else' on the ridge (eg. walking the dog).</p>

    <h3>Weeding</h3>

    <p>Yes, weeding! Weeding can be suprisingly adictivity. Once you start, it can be hard to stop.</p>

    <p>You don't need attend group meetings or have an individual area to help with the weeding. If you go for a walk on the ridge every day and pull out a hundred weeds each time, this can have a big impact overtime.</p>

    <p>Many people have a favourite weed (e.g fleabane, verbascum, wort, etc). They whack the weed whereever they see it.</p>

    <h3>Surveying</h3>

    <p>Helping out on one of the groups many surveys is a great way to contribute if you do not want to get your &quot;hands dirty&quot;. Surveying does not have to involve a lot of time or effect and can normally be done while doing something else on the ridge.
        Surveying can be fun and you can learn a lot about the ridge doing it.</p>

    <p>Surveying can be a great way to create interest in the ridge and help other people to contribute to the work of the group.</p>

    <h3>Newsletter</h3>

    <p>The monthly newsletter is an important way of communicating information about our group, our ridge and our work</p>

    <p>The newsletter is always in need of good content. If you can contribute an article or photo, then please do, whether it is on an ad hoc or more regular basis.</p>

    <p>Desktop publishing skills are also appreciated.</p>

    <h3>Photography</h3>

    <p>Photos are a great way of recording the progress of the work on the ridge. The group needs photographers of any standard just to take regular photos.</p>

    <p>Photos are also a great way of promoting the ridge and the work we do on it.</p>

    <h3>Rubbish Collection</h3>

    <p>There are unfortunately bogans on the ridge. They leave the rubbish. If you see some, please just pick it up. Many members pick up rubbish and if you do it, then it is one less thing that they have to do and can spend the time doing something else that you don't do (e.g weeding)</p>

    <h3>Web Site Content</h3>

    <p>If you have some information that you think may be valualbe please let us know, no matter how small. If you have a idea for a new page or section then let us know.</p>

    <h3>Website Development</h3>

    <p>You can help with improving and maintaining this website. If you have some web development skills (graphic design, programming, database administration, web hosting, etc), then we can find something for you to do.</p>







