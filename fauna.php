            </p>
            Wombats are stout marsupials between 90cm and 120 cm long and can weigh up to 36 kg. They have a large, blunt head and a short, neck. Despite their slow appearance they can run quite fast. Their sharp claws and stubby, powerful legs make them great diggers. They dig burrows and tunnels in the ground for shelter and to escape from danger. Wombats can live for up to 27 years in captivity.
            Habitat
            Wombats are usually found in wet, partly
            forested areas on the coast, and on mountain
            ranges. Wombats live in burrows, preferring
            well-drained soils that are easy to dig in. The
            burrows can be up to 30m long, and several
            metres deep. In Canberra wombats have often
            been seen in Tuggeranong near Gordon and
            Banks. There is not a lot of information out
            there on their distribution in Canberra (note-
            Anna See).
            Food Native grasses such as wallaby grass and kangaroo grass, sedges and roots of shrubs and trees. They cut their food with sharp front teeth which grow continuously. Breeding Mating occurs between September and December, and usually results in one offspring. The newborn wombat, which weighs only 1 g and is less than 3 cm long crawls into the mother's pouch. This pouch faces backwards to stop dirt getting into it when mum is digging. The young wombat will stay in the pouch for between 7 and 10 months.
