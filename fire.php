            <h2>Webmaster's Note</h2>

            <h3>Photographs</h3>

            <p>
                Some photos belonging to members have been destroyed in the fire. If
                you can donate photos to be included in the group's archives, please contact
                us. Our members collect data and photos about the Ridge's vegetation for
                scientific purposes. We plan to scan all photos and burn them to a CD, keeping
                a backup copy in a different location to the originals.
            </p>

            <h3>Regeneration</h3>

            <p>
                We have been very excited by the regrowth of some of the flora and the
                appearance of certain natives in sections where they weren't seen before.
                Most are small, however, and you need to know what you are looking for to
                see them. To the untrained eye the Ridge is just a large expanse of exotic
                grasses, but underneath are hidden treasures. Did you notice the pictures
                of ferns on this site? They are native to the area and are used in the group's
                logo. They have come back strongly. Lomandras (at first glance a bit like
                grass) grew in large numbers last summer. Cooleman Ridge is suffering more
                from the drought than from the fires, which caused some loss of trees but
                benefitted other plants.
            </p>

            <p>
                New members are always welcome. The area will recover in time: if you
                want to help us help it along, please contact us. You may not, for instance,
                know that we were beginning to plant native grasses on the Ridge: these
                are better than pasture grasses in a fire, as they tend to stay greener
                near the ground, reducing the fuel load available to the fire. Off-the-top-of-the-head
                estimates suggested it would take 50-100 years to replace all the grasses
                with the few resources we have - all the more reason to start now!
            </p>

