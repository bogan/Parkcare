    <h2 class="page-header">Fire</h2>

    <p>Fire is an important and sometimes controversial part of the ecology and long term management of the ridge. This page is intended to give a brief overview of the history, role and management of fire on the ridge.</p>

    <h3>History</h3>

    <!-- Today -->

    <p>The history of fire on the ridge has strongly followed the <a href="history.php">history</a> of the ridge itself. the role of humans has been the most significant factor in the frequency, intensity and effect of fires on the ridge.</p>

    <p>
        Fire has been present on the ridge for millions of years.
    </p>

    <p>
        Artificial burning was introduced by Aborigines who regularly burned, low intensity burns. to better manage the land for their purposes
    </p>

    <p>
        European settlers generally believed that fire was only bad as it not only destroyed food but risked destroying infrastructure such as fences, sheds and homes. Fire was therefore generally suppressed.
    </p>

    <p>
        When the ridge was converted from a grazing property to a nature service in the 1980s, very few fires occurred on the ridge prior to January 2003.
    </p>

    <p>
        Since January 2003 the ridge nowdays can be planned or unplanned. Planned fires are conducted by the rural fire service with the objective being to reduce the amount of fuel. This helps reduce the possibility and intensity of an uncontrolled fires on the ridge.
    </p>

    <p>
        Since the 2003 fires, fires have been more actively managed.
    </p>

    <h3>Management</h3>

    <p>
        Fires on the ridge our now actively managed by the ACT Parks and Conservation Service.
    </p>


    <p>
        Prescribed burns have been a regular almost annual feature of the fire management on the ridge since 2003.
    </p>

    <p>
        Prescribed burns typically target only a small area of the ridge at a time. Progressively overtime, the ridge is burnt. This pattern of burning mosaic burning.
    </p>

    <p>
        The use of controlled of hazard reduction burns has many secondary benefits on the ridge.</p>
        <a href="http://www.environment.act.gov.au/ACT-parks-conservation/bushfire_management/prescribed-burns">hazard reduction burn programme
    </a>


    <h3>Weeds</h3>

    <p>
        The ridge has a significant weed problem. These weeds also play an important factor in the role of fire on the ridge.
    </p>

    <p>
        Weeds are important to fires because some fires are major fuel source for fires. Several weeds found on the ridge are pasture grasses (such as Phalaris, Rye Grass, Fescue and Wild Oats). These grasses were were deliberately introduced by former graziers when the ridge used to be part of pastoral properties. These grasses were introduced because they were considered to be a better food source for large grazing animals (eg. horses, cows, sheep). They have a much greater biomass because they are typically thick, tall and fast growing.
    </p>

    <p>
        These weeds are significant to the presence of fire on the ridge because they have now become a major <span class="highlight">fuel source</span>.

        The very qualities that made these plants good pasture grass are the very qualities that make them a significant fire hazard on the ridge. Because they have a much greater biomass, they burn fast and at a higher temperature and therefore can significantly increase the speed and intensity of grass fires.
    </p>

    <p>
        The fire problem associated with pasture grasses has become serious with <span class="highlight"> the removal of grazing</span> from the ridge.
        These grasses when grazed regularly grazed have a minimal affect. These weeds were not a major fuel problem when they were actively grazed. However, now that regular grazing has been removed, they become a more significant problem.
        Introduced pasture grasses when they are no longer grazed pose a significant fire hazard.
         However, when they are no longer grazed, they dry out.
    </p>

    <p>
        Most <span class="highlight">native grasses</span> on the ridge, in contrast, are must less of a fire hazard.

        Native grass are generally much smaller and thinner and therefore burn slower and cooler than many pasture grasses.

        The biomass of many weeds/pasture grasses is much greater than that of many natives  and thus the fuel load of <span class="highlight">native grasses and forbes</span> present on the ridge is generally much lower.

    </p>




    <p>
        The group plays an important role in reducing the risk, intensity and impact of unplanned fires.

        The long term goal is to remove the pasture grasses and thereby significantly reduce the associated fire hazard.

        By replacing introduced pasture grasses with native grasses, the fuel load is significantly reduced.
    </p>

    <h3>Cattle Grazing</h3>

    <p>
        Cattle Grazing trials have been performed on the ridge as a fire management technique.
    </p>

    <h3>Regeneration</h3>

    <p>
        A major secondary benefit of controlled burns is that these burns can signficantly accelerate the regeneration of the bush.
    </p>

    <p>
        A balanced fire management regime can have significantly assist in the regeneration of the bush. Too much and things don't do well. To little and things don't do well.
    </p>

    <p>
        Most of the group areas have had one or more burns on them. This has significantly enhanced the ratural regeneration process.
    </p>

    <p>
        Native generally respond better to regular burns than exotics. Burns help accelerate the regeneration of native plants and grasses in particular.
    </p>

    <p>
        Prescribed burns also provide a great opportunity for the group. The group takes advantage of control burns as a good opportunity. Vegetation cover is reduced, weeds are more visible and easier to remove.
    </p>

    The right balance between too much and too little fire.

