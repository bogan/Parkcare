<div class="panel panel-success">
    <div class="panel-heading">Blackthorn <em>(Bursaria spinosa ssp lasiophylla)</em></div>
    <div class="panel-body">

        <img src="images/blackthorn.jpg" style="height:100px;float:left;padding:0px;margin: 5px"/>

        <p>
            <a href="https://en.wikipedia.org/wiki/Bursaria_spinosa">Blackthorn</a> is a medium sized shrub. It has small dark green waxy leaves and small thorns. It is fairly slow growing but a very hardy plant that copes well with both drought and fire. It flowers around Christmas time with a profusion of small creamy colour flowers.
        </p>

        </p>
            Blackthorn performs many important ecological functions on the ridge such as being a habit for insects and birds that feed on insects that attach larger plants such as Red Gum.
        <p>

        <p>
            Blackthorn is fairly widespread on the ridge. It was probably common on many of the lower parts of the ridge but has been cleared deliberately or disappeared because of grazing pressure. It is often found on the steeper slopes of the ridge. It is naturally spreading.
        </p>

        <p>
            The best examples of Blackthorn are found on the southern slopes of Mt Arawang and below the management trail above Kathner Street.
        </p>

    </div>
</div>

