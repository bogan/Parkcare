<h2 class="page-header">Group History</h2>

    <p>The group was formed in 1991 by three ladies - Bess Sledge, Pauline Lynga and Tina Nevins. </p>

    <p>The group has now grown to have 50 registered members that contribute in various ways with small but active core of members that regulary meet and work on the nature reserve.</p>

    <p>The original focus of the group was to plant new plants</p>

    <p>Over the years, the focus of the group has switched focus from plantings to weeding. This is inline with the groups philosopy about how to most effectively restore the ecology of the group.</p>
    <p>The group has made significant progress with rehabilitating the reserve but much work remains to be done.</p>

