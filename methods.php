    <p>The main reason why weeding is more effective than planting is that the bush has a natural tendency to regenerate anyway, whether humans help or not. However, humans can great significantly accelerate the natural regeneration process by removing competitors that prevent the bush from regeneration. Weeds compete for light, water and nutrients. Removing weeds allows struggling natives to maximise the use. The idea is to create an empty space for the bush to move into. The bush will do this naturally. All it requires from us is to create this space. This is the main reason for weeding - to remove competition and create a space.

<p>Weeding should by lightweight rather than heavy weight. Weeding is most effective in strong areas rather than week areas. Many think that weeding is futile because they will just come back. Once a good native cover is established, most weeds rarely reenter. Work with the bush rather than against it.  </p>


