    <p>The Cooleman Ridge Parkcare group uses its own variation of the <a href="https://en.wikipedia.org/wiki/Bush_regeneration#Bradley_method">Bradley Method</a>  to regenerate the bush. The effectiveness of this method is proven and is now widely accepted throughout Australian "landcare" groups as the most cost effective method to regenerated the bush.

    <p>The Bradley Method is however counter intuitive to those unfamiliar with it. This is because it emphasises "weeding over planting". It maintains that an hours weeding gives a much better return than an hours planting. Weeding is by no means a futile activity. A well targeted and executed weeding strategy can have achieve major results quickly. The main advantages of weeding over planting are summarised in the following table:
