<h2>News</h2>

    <p>
        News about our group, our ridge and our work is published via our newsletter. It is published monthly, approximately 10 days before our regular monthly meeting (i.e around the 10th of each month). It is distributed via email in PDF format.
    </p>

    <p>
        The newsletter contains news of <a href="meetings.php">group meetings</a>, <a href="plants.php">plant sitings</a>, <a href="animals.php">animal sitings</a>, government initiatives, etc. The main highlight is a section called "What's Around" segment, which includes details about a particular plant or animal specie on the ridge.
    </p>

    <p>
        Many people subscribe to the newsletter and is our bestway of engaging the community. If you want to subscribe or unsubscribe to the newsletter, please send us an email via the <a href="contact.php">Contact</a> page.
    </p>

    <p>
        Previous versions of the newsletter are stored on this website and are listed in the table below.
    </p>

