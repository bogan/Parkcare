<h2 class="page-header">Partners</h2>

    <p>
        The group works with a number of other organisations to help care for the Cooleman Ridge Nature Reserve. These include:
    </p>

    <h3 id="parksandconservation">ACT Parks and Conservation Service</h3>

    <img src="images/parks.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px "/>


    <p>The <a href="http://www.environment.act.gov.au/ACT-parks-conservation">ACT Parks and Conservation Service</a> is the main government agency responsible for the daily management of the nature reserve.</p>

    <p>The ACT Parks and Conservation Service assists the group by providing information and general support to the group.</p>

    <h3>Southern ACT Catchment Group</h3>

    <img src="images/sactcg.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p>The <a href="http://www.environment.act.gov.au/ACT-parks-conservation">Southern ACT Catchment Group</a> is the main government agency responsible for the daily management of the nature reserve.</p>

    <p>The Southern ACT Catchment Group assists the group by providing information and general support to the group.</p>

    <h3>Green Army</h3>

    <img src="images/greenarmy.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p>The <a href="http://www.environment.gov.au/land/green-army">Green Army</a> helps out regularly performing many hands on jobs such as weeding, spraying, planting.</p>

    <p>The Green Army assists the group by providing information and general support to the group.</p>

    <h3>Landcare Australia</h3>

    <img src="images/landcare.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p><a href="https://landcareaustralia.org.au/">Landcare Australia</a> helps out regularly performing many hands on jobs such as weeding, spraying, planting.</p>

    <p>The Green Army assists the group by providing information and general support to the group.</p>

    <h3>Canberra Nature Map</h3>

    <img src="images/canberranaturemap.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p><a href="https://canberra.naturemapr.org.au/">Canberra Nature Map</a> is web site based on volunteers who contribute sightings of various plants, animals, etc in the various nature reserves throught the ACT and adjoing areas.</p>

    <p>Canberra Nature Map assists the group by allowing the group to track the location of significant species on the ridge. It complements several of our own surveys such as the <a href="plants.php">Plant Survery</a>, the <a href="animals.php">Animal Survey</a>.</p>

    <h3>Australian National Botanical Gardens</h3>

    <img src="images/anbg.jpg" style="height:100px;width:100px;float:left;padding:5px;margin: 5px"/>

    <p><a href="https://www.anbg.gov.au/">Australian National Botanic Gardens</a> is Australias premier scientific organisation on Australian flora.</p>

    <p>The group often liaises with the Australian National Botanic Gardens to help identify plant species found on the ridge.</p>

