				<h2>Recent History</h2>
		
				<p>Cooleman Ridge has been grazed by sheep 
				and cattle for over a hundred years and consequently has many exotic grasses 
				and weeds. As part of 'Yarralumla' property, 1899 maps indicate open forest. 
				Latterly it was part of the 'Cooleman' grazing property when it was sown 
				with <i>Phalaris</i> pasture grass and fertilised. It has been part of Canberra 
				Nature Park formally since 1993 but was a reserve before that.</p>
		
