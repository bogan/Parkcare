    <h3>Techniques</h3>



    <p>
        Many different weeding techniques are used on the ridge, both by the group itself as well its partners. These include:


    </p>

    <div id="hacking" class="panel panel-success">
        <div class="panel-heading">Hacking</em></div>
        <div class="panel-body">

            <img src="images/tina.png" style="height:100px;float:left;padding:0px;margin: 5px"/>

            <p>
                Hacking is the main form of weeding performed the group. It is generally performed with a small pick and simply involves digging out small weeds.
            </p>

            <p>
                Hacking  is most effective against small to medium herbaceous weeds that have a distinctive rosette, such as <a href="weeds.php#aaronsrod">Aaron's Rod</a>, <a href="weeds.php#wildsage">Wild Sage</a>, <a href="weeds.php#scotchthisle">Scotch Thistle</a> and <a href="weeds.php#pattersonscurse">Patterson's Curse</a>. Large areas can be clear quickly with minimal effort. It is also a very precise method that removes the weed while leaving natives and is great way to regenerate native grasslands.
            </p>

        </div>
    </div>

    <div id="poisoning" class="panel panel-success">
        <div class="panel-heading">Poisoning</em></div>
        <div class="panel-body">

            <img src="images/jan.png" style="height:100px;float:left;padding:0px;margin: 5px"/>

            <p>
                Poisoning involves the application of some herbicide to the plant. The herbicide is generally a <a href="https://en.wikipedia.org/wiki/Glyphosate">glyophasate</a> based one that does not harm the enviroment nor humans. Some other more specialised poisons are sometimes used for specific weeds.
            </p>

            <p>
                Poisons are typically applied using two different methods - spraying or cut-and-dabbing. Spraying is most effective against weeds that have a large rosette such as <a href="weeds.php#pattersonscurse">Pattersons Curse</a> whereas cut-and-dab is best used with woody weeds such as <a href="weeds.php#sweetbriar">Sweet Briar</a>.
            </p>

            <p>
                Spraying of specific weeds such as <a href="weeds.php#blackberry">Blackberry</a> is performed by Parks and Conservation officers (or their contractors) using commercial level equipment.
            </p>

        </div>
    </div>

    <div id="beheading" class="panel panel-success">
        <div class="panel-heading">Beheading</em></div>
        <div class="panel-body">

            <img src="images/pt.jpg" style="height:100px;;float:left;padding:0px;margin: 5px"/>

            <p>
                Beheading involves the removal of the head or seeding part of the plant while leaving the rest of the plant in place. The head is removed either with some cutting implement or simply by hand.
            </p>

            <p>
                Although this does not kill the plant, it prevents it from reseeding and thus prevents any future generations from replacing the plant when that plant eventually dies. A concerted course of this technique over several seasons will reduce or eliminate the weed.
            </p>

            <p>
                This technique is best used with slower growing, annual, tall weeds such as <a href="weeds.php#aaronsrod">Aaron's Rod</a> and <a href="weeds.php#fleabane">Fleabane</a>.
            </p>

        </div>
    </div>

    <div id="burning" class="panel panel-success">
        <div class="panel-heading">Burning</em></div>
        <div class="panel-body">

            <img src="images/verbascum.jpg" style="height:100px;width:100px;float:left;padding:0px;margin: 5px"/>

            <p>
                Burning can be effective against some weeds that are not very fire tolerant. A regular burn of infested errors with gradually reduce these weeds over time.
            </p>

            <p>
                Burning not only kills many weeds but more importatly triggers the native plants to germinate. See our <a href="fire.php">Fire</a> page for more details.
            </p>

            <p>
                Burning is not directly used by the group but is performed by the ACT Rural Fire Service as part of their <a href="http://www.environment.act.gov.au/ACT-parks-conservation/bushfire_management/prescribed-burns">hazard reduction burn programme</a>. Although the primary intention is not to reduce weeds, burning has many secondary benefits for weed control because it not only kills many weeds but more importatly triggers the native plants to germinate. See our <a href="fire.php">Fire</a> page for more details.
            </p>

        </div>
    </div>

    <div id="smothering" class="panel panel-success">
        <div class="panel-heading">Munching</em></div>
        <div class="panel-body">

            <img src="images/verbascum.jpg" style="height:100px;width:100px;float:left;padding:0px;margin: 5px"/>

            <p>
                Munching involves using biological agents such as insects to simply eat the weeds. The muchning may kill the plant or may simply weaken it. Alternatively, it may prevent it from reproducing.
            </p>

            <p>
                Biological controls exist for some weeds such as <a href="weeds.php#stjohnswort">St Johns Wort</a> and <a href="weeds.php#pattersonscurse">Pattersons Curse</a>.
            </p>

        </div>
    </div>

    <div id="smothering" class="panel panel-success">
        <div class="panel-heading">Smothering</em></div>
        <div class="panel-body">

            <img src="images/doug.png" style="height:100px;float:left;padding:0px;margin: 5px"/>

            <p>
                Smothering or mulching involves covering weeds with some substance such as newspaper, plastic sheets or even the remains of other weeds. Smothering is limited by the availability of materials to smother with.
            </p>

            <p>
                The blocking of the light starves the weed of light and it can not photosynthesise and eventually dies of starvation.
            </p>

            <p>
                Smothering is best used on thick infestations of weeds that are difficult to control with other weeds. such as <a href="weeds.php#phalaris">Phalaris</a>.
            </p>

        </div>
    </div>

    <p>
        Each technique is target at the specific weeds concerned. Some weeds are more effectively managed than others.
    Each has its place in the toolkit.
    </p>


    <p>
        Many major weeds our now largely extinct from the ridge and many are well on their way to be declaring them extinct.
    </p>


