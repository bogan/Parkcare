    <h2 class="page-header">Trails</h2>

    <p>The Cooleman Ridge Nature Reserve is lucky to be home to a wide variety of tracks that are of local, regional and national significance.</p>

    <p>These various trails serve a variety of purposes ranging from recreation to management.</p>

    <p>These trails play an important role for connecting the work of the group with the broader community.</p>

    <h3>Bicentennial National Trail (BNP)</h3>

    <p>The <a href="www.bicentennialnationaltrail.com.au">Bicentennial National Trail</a> is a 5000km multipurpose trail that runs from Healesville Victoria to Cooktown Queensland.
    Part of this trail crosses the Cooleman Ridge Nature Reserve. The trail is mainly used by equestrian riders but people have been known to travel using other forms of transport such as feet, donkeys and even camels!
    </p>

    <p>The Bicentennial National Trail is the only part of the nature reserve that horses are allowed on.</p>

    </p>

    <h3>Canberra Centenary Trail</h3>

    <p>The <a href="http://www.environment.act.gov.au/parks-conservation/parks-and-reserves/find-a-park/rural/canberra-centenary-trail">Canberra Centenary Trail</a> is an ACT based multipurpose track that circumnavigates the city of Canberra.</p>

    <p>The track was created in 2013 to celebrate the 100th anniversery of the founding of the city of Canberra.</p>

    <p>The section that cross the nature reserve is composed of both existing management vehicular trails as well as specially contrusted walking trails.</p>

    <p>The section that crosses the Cooleman Ridge offers some of the best views that can be seen along the trail, looking out over the Murrumbidge River valley and beyond to the Bullen, Tidbinbilla and Brindabella ranges.</p>

