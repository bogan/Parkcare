    <h2 class="page-header">Water</h2>

    <p>The ridge is unfortunately lacking in significant water course.</p>

    <h2>Creeks</h2>

    <p>Cooleman Ridge is the headwaters for 4 creeks.</p>

    <ul>
        <li><strong>Bulgar Creek</strong> - located on the north west of the ridge and flows to the Murrumbidgee River.</li>
        <li><strong>Weston Creek</strong> - located on the north east side of the ridge and flows into the Molonglo River.</li>
        <li><strong>Village Creek</strong> - located on the the south east end of the ridge and flows into Lake Tuggeranong.</li>
        <li><strong>New Station Creek</strong> - located on the south west edge of the ridge and flows into the Murrumbidge River.</li>
    </ul>

    <p>There are no permanent water courses found on the ridge. The creeks are intermittent and only flow for a short time after heavy rain.</p>

    <h3>Dams</h3>

    <p>
        There are however three dams found on the ridge:

        <ul>
            <li><strong>The Old Dam</strong> - located on the headwaters of New Station Creek on the south western slopes of the ridge.</li>
            <li><strong>The Kathner Street Dam</strong> - located on the headwaters of Bulgar Creak on the north western edge of the ridge.</li>
            <li><strong>The Dry Dam</strong> - located on the south eastern of the ridge on the south western slopes of Mt Arawang</li>
        </ul>

    <p>
        These dams were created principally to support cattle grazing on the ridge, either prior to the ridge becoming a reserve (eg. The Old Dam and Dry Dams) or after it (e.g. Kathner Street Dam). A secondary reason for their creation was to help in firefighting in the event of a fire occuring on the ridge.
    </p>

    <p>
      These dams serve as important habitats for various species including fish, ducks, turtles and insects. They play an important role in preserving the bio diver
    </p>


    <p>
        These dams have been survey over the years to assist in monitoring their health. Please see our <a href="dams.php">Dam Survey</a> page for more details.
    </p>

    <h3>Stormwater</h3>

    <p>The north western slopes are surround by a large storm water cut off drain. The main purpose of this drain is to prevent excess water runoff from the ridge into the houses belows the ridge in the adjacent suburbs of Kambah and Chapman.</p>

