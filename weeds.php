            <p>Blackberry is only effectively treated with poisoning. However, it is effectively beyond the capability of the group to control some of the the larger infestations. These locations are then communicated to Parks and Conservation workers/contractors who use inustrial standard spraying equipment. Blackberrys are typcially sprayed in late summer or autumn when they are actively growing and after  fruiting.</p>

