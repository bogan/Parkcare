    <h2 class="page-header">Weeds</h2>



    <p>Weeds and weeding are the main focus of the group. There are large number of different types of weeds on the ridge. However, there are roughly a dozen weed species that constitute the main bulk of the weeds on the ridge.</p>




    <p>The group believes that it is important that everybody be able to recognise the weeds, even if they do not actively weed themselves. The first step in helping to solve the problem is to identify the problem. You can help by simply being aware what are weeds and what are not. Most inexperienced people often confuse natives and exotics with one another. </p>

    <p>This page is here to give somebody new to the ridge a gentle introduction to its most signficant weeds.  Each species is quite easy to identify, even for a novice. The following is a shortened list of the main weeds that the Group focuses on. For a complete list of all weeds found on the Cooleman Ridge Nature Reserve, please see our <a href="https://en.wikipedia.org/wiki/Bush_regeneration#Bradley_method">Plant Survey</a>.</p>

